create table if not exists public.classificacao_regras (
  id bigint generated by default as identity primary key,
  match_field text not null check (match_field in ('prefixo', 'codigo')),
  match_value text not null,
  status_destino text check (status_destino in ('SERVICO', 'TESTE')),
  tipo_caixa_destino text check (tipo_caixa_destino in ('Oitavada', 'Maleta', 'Quadrada', 'Flexo', 'Outro')),
  tamanho_destino text,
  prefixo_destino text,
  active boolean not null default true,
  created_by uuid references auth.users(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint classificacao_regras_match_unq unique (match_field, match_value)
);

create index if not exists classificacao_regras_match_idx
  on public.classificacao_regras (match_field, match_value);

